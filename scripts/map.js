const DEFAULT_STYLES = ["lightgrey", "darkgrey"]

class TileSet {
    constructor(data) {
        this.tileWidth = 0;
        this.tileHeight = 0;
    }

    draw(g) {
    }
}

TileSet.DEFAULT = {
    tileWidth: 32,
    tileHeight: 32,
    draw: function (g, tile, x, y) {
        g.fillStyle = DEFAULT_STYLES[tile];
        g.fillRect(
            x * map.tileWidth - 0.25,
            y * map.tileHeight - 0.25,
            map.tileWidth + 0.5,
            map.tileHeight + 0.5);
    }
};

class TileMap {
    constructor(data) {
        this.tileset = data.tileset || TileSet.DEFAULT;
        this.tileWidth = this.tileset.tileWidth;
        this.tileHeight = this.tileset.tileHeight;
        this.offsetX = data.offset.x;
        this.offsetY = data.offset.y;
        this.tiles = data.tiles;
        this.width = this.tiles[0].length;
        this.height = this.tiles.length;
        this.collision = data.collision;
    }

    draw(g) {
        g.save();
        {
            g.translate(this.offsetX * map.tileWidth, this.offsetY * map.tileHeight);
            for (let y = 0; y < this.height; ++y) {
                for (let x = 0; x < this.width; ++x) {
                    const tile = this.tiles[y][x];
                    this.tileset.draw(g, tile, x, y);
                }
            }
        }
        g.restore();
    }
}

